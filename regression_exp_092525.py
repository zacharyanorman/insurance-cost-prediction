import pandas as pd
import numpy as np
import statsmodels.api as sm
import matplotlib.pyplot as plt

# Fake data

np.random.seed(42) 
# Makes the "random" numbers identical, 42 is arbitarary

n = 500 
# Number of data points created 

advertising = np.random.uniform(1000, 5000, n) 
# uniform pulls truly "uniformly scattered" random numbers

employees = np.random.randint(44, 50, n)
num_clients = np.random.randint(200,800, n)

sales = 50 + 0.05*advertising + 2*employees + 3.5*num_clients + np.random.normal(0, 100, n) 
# normal pulls from numbers around the mean

df = pd.DataFrame({"Advertising": advertising, "Sales": sales, "Employees": employees, "Clients": num_clients})
# creates a 30x3 dataframe with columns Advertising and Sales


# Regression

X = df[["Advertising", "Employees", "Clients"]]
X = sm.add_constant(X)
# creates a column of repeated 1.0s to allow model to 'fit' around intercept

model = sm.OLS(df["Sales"], X).fit()
# creates an OLS linear regression model (fitting a line over the data) for sales,
# based on the data in X

print(model.summary())
# prints a summary of each of the values generated by the model